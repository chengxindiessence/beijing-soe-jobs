def write_catalog(filename: str, title: str, enterprises: list[dict]) -> None:
    CATALOG_DIR.mkdir(parents=True, exist_ok=True)
    path = CATALOG_DIR / filename
    data = {"title": title, "enterprises": enterprises}
    path.write_text(
        yaml.dump(data, allow_unicode=True, sort_keys=False, default_flow_style=False),
        encoding="utf-8",
    )
    print(f"Wrote {path} ({len(enterprises)} entries)")


def main() -> None:
    write_catalog("01_sasac_military.yaml", "国资委央企-军工航天", build_sasac_military())
    write_catalog("02_sasac_energy.yaml", "国资委央企-能源石油电网", build_sasac_energy())
    write_catalog("03_sasac_industry.yaml", "国资委央企-制造基建等", build_sasac_telecom_industry())
    write_catalog("04_mof_finance.yaml", "财政部管理央企-金融文化", build_mof_finance())
    write_catalog("05_beijing_municipal.yaml", "北京市属国企", build_beijing_municipal())
    write_catalog("06_public_institutions.yaml", "事业编单位", build_public_institutions())
    total = sum(
        len(yaml.safe_load((CATALOG_DIR / f).read_text())["enterprises"])
        for f in sorted(CATALOG_DIR.glob("*.yaml"))
    )
    print(f"Total catalog entries: {total}")


